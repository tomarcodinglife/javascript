// immediately invoked function expression (IIFE)   

// you can use this type IIFe with normal function
(
    function functionName () {
        console.log("Sujit Kumar Singh")
    }
)();




(() => {console.log("sujit Tomar")}
)();





((myName) => {
    console.log(`${myName}`)
}
)("sujit");


((myName) => {
    console.log(myName)
}
)("sujit");



